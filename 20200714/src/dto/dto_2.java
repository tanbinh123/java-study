package dto;

public class dto_2 {

	String name, area;
	int age;
	
	
	public dto_2(String name, String area, int age) {
		this.name = name;
		this.area = area;
		this.age = age;
	}


	public String getName() {
		return name;
	}


	public String getArea() {
		return area;
	}


	public int getAge() {
		return age;
	}
	
	
	
	
}
