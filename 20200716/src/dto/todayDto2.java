package dto;

public class todayDto2 {
	String no, name, depart, position;

	public todayDto2(String no, String name, String depart, String position) {
		super();
		this.no = no;
		this.name = name;
		this.depart = depart;
		this.position = position;
	}

	public String getNo() {
		return no;
	}

	public String getName() {
		return name;
	}

	public String getDepart() {
		return depart;
	}

	public String getPosition() {
		return position;
	}
	
	
}
